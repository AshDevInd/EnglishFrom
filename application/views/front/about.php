       phonetic. Roman letters are not particulary useful for Khmer. The biggest problem is 
       with the phontic inconsistency of Roman letters. Devangari is much more useful, as long 
       as you can read Modified Devangari (MD), but the sounds do differ from Indian languages 
       that use traditional Devangari.
    </p>
